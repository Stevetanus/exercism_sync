/**
 * Create an appointment
 *
 * @param {number} days
 * @param {number} [now] (ms since the epoch, or undefined)
 *
 * @returns {Date} the appointment
 */
export function createAppointment(days, now = undefined) {
  now = now ? now : new Date()
  return new Date(now + days * 24 * 60 * 60 * 1000)
}

/**
 * Generate the appointment timestamp
 *
 * @param {Date} appointmentDate
 *
 * @returns {string} timestamp
 */
export function getAppointmentTimestamp(appointmentDate) {
  console.log({appointmentDate})
  return new Date(appointmentDate).toISOString()
}

/**
 * Get details of an appointment
 *
 * @param {string} timestamp (ISO 8601)
 *
 * @returns {Record<'year' | 'month' | 'date' | 'hour' | 'minute', number>} the appointment details
 */
export function getAppointmentDetails(timestamp) {
  const date = new Date(timestamp)
  return {
    year: date.getFullYear(),
    month: date.getMonth(),
    date: date.getDate(),
    hour: date.getHours(),
    minute: date.getMinutes(),
  }
}

/**
 * Update an appointment with given options
 *
 * @param {string} timestamp (ISO 8601)
 * @param {Partial<Record<'year' | 'month' | 'date' | 'hour' | 'minute', number>>} options
 *
 * @returns {Record<'year' | 'month' | 'date' | 'hour' | 'minute', number>} the appointment details
 */
export function updateAppointment(timestamp, options) {
  const appointmentDetails = getAppointmentDetails(timestamp)
  const newAppointmentDetails = {...appointmentDetails, ...options}
  const date = new Date(newAppointmentDetails['year'], newAppointmentDetails['month'], newAppointmentDetails['date'], newAppointmentDetails['hour'], newAppointmentDetails['minute'])
  return getAppointmentDetails(date)
}

/**
 * Get available time in seconds (rounded) between two appointments
 *
 * @param {string} timestampA (ISO 8601)
 * @param {string} timestampB (ISO 8601)
 *
 * @returns {number} amount of seconds (rounded)
 */
export function timeBetween(timestampA, timestampB) {
  const timeSecsA = Math.round(new Date(timestampA) / 1000)
  const timeSecsB = Math.round(new Date(timestampB) / 1000)
  return timeSecsA > timeSecsB ? timeSecsA - timeSecsB : timeSecsB - timeSecsA
}

/**
 * Get available times between two appointment
 *
 * @param {string} appointmentTimestamp (ISO 8601)
 * @param {string} currentTimestamp (ISO 8601)
 */
export function isValid(appointmentTimestamp, currentTimestamp) {
  return appointmentTimestamp > currentTimestamp
}
