"""
This module has a 'two_fer' function help you say generous phrase.
"""


def two_fer(name="you"):
    """One for a person, one for me.
 
    :param name: str - the person's name you will give cookie to, default is you.
    :return: str - the sentence you will say to the person.
    """
    return f"One for {name}, one for me."
